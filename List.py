countries = ['Kenya', 'Jamaica', 'Egypt', 'Nigeria']
print(countries)

#To get a certain value and to get a character
print(countries[1]) #value
print(countries[0][1]) #character 

#To display form one point to another
print(countries[1:]) #From index 1 till the end
print(countries[0:3]) #Doesn't display Nigeria

#To get type of list
print(type(countries))

#To change value
countries[1] = 'Ghana'
print(countries)

